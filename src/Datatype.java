public class Datatype {
    public static void main(String[] args) {
        int items = 50;
        float price = 9.55f;
        double amount = items * price;

        System.out.println("Number of items are : " + items);
        System.out.println("Price per items : " + price);
        System.out.println("Total amount to be paid : " + amount);
    }

}
